<h4 class="h5 id-green">4. Violations of Insider Trading Laws</h4>
<p>Penalties for trading on or communicating material non-public information can be severe, both for individuals involved in such unlawful conduct and their employers and supervisors, and may include jail terms, criminal fines, civil penalties and civil enforcement injunctions. Given the severity of the potential penalties, compliance with this Policy is absolutely mandatory.</p>

<p><i class="fas fa-circle"></i>(a) Legal Penalties. A person who violates insider trading laws by engaging in transactions in a company's securities when he or she has material non-public information can be sentenced to a substantial jail term and required to pay a penalty of several times the amount of profits gained or losses avoided.

 

In addition, a person who tips others may also be liable for transactions by the tippees to whom he or she has disclosed material non-public information. Tippers can be subject to the same penalties and sanctions as the tippees, and the SEC has imposed large penalties even when the tipper did not profit from the transaction.

 

The SEC can also seek substantial penalties from any person who, at the time of an insider trading violation, "directly or indirectly controlled the person who committed such violation," which would apply to the Company and/or management and supervisory personnel. These control persons may be held liable for up to the greater of $1 million or three times the amount of the profits gained or losses avoided. Even for violations that result in a small or no profit, the SEC can seek a minimum of $1 million from a company and/or management and supervisory personnel as control persons.</p>

<p><i class="fas fa-circle"></i>(b) Company-imposed Penalties. Employees who violate this Policy may be subject to disciplinary action by the Company, including dismissal for cause. Any exceptions to the Policy, if permitted, may only be granted by the Compliance Officer and must be provided before any activity contrary to the above requirements takes place.</p>



