<h4 class="h5 id-green">3. Definitions</h4>
<p><i class="fas fa-circle"></i>(a) Materiality. Insider trading restrictions come into play only if the information you possess is "material." Materiality, however, involves a relatively low threshold. Information is generally regarded as "material" if it has market significance, that is, if its public dissemination is likely to affect the market price of securities, or if it otherwise is information that a reasonable investor would want to know before making an investment decision.

 

Information dealing with the following subjects is reasonably likely to be found material in particular situations:

 

(i) significant changes in the Company's prospects;

 

(ii) significant write-downs in assets or increases in reserves;

 

(iii) developments regarding significant litigation or government agency investigations;

 

(iv) potential financial liquidity problems;

 

(v) changes in earnings estimates or unusual gains or losses in major operations;

 

(vi) changes in management;

 

(vii) changes in dividends;

 

(viii) extraordinary borrowings;

 

(ix) award or loss of a significant contract;

 

(x) changes in debt ratings;

 

(xi) proposals, plans, agreements or news, even if preliminary in nature, involving mergers, acquisitions, divestitures, recapitalizations, strategic alliances, licensing arrangements, tender offers, purchases or sales of substantial assets or subsidiaries;

 

(xii) public offerings;

 

(xiii) pending statistical reports (such as, commodity prices, money supply and yield estimates, or interest rate developments); and

(xiv) changes in dividend policies or the declaration of a share split or the offering of additional securities.

 

Tipping occurs when an individual passes material nonpublic information on to others or recommends to anyone the purchase or sale of any securities when they are aware of such information.  This practice also violates the securities laws and can result in the same civil and criminal penalties that apply to insider trading, even though the tipper did not trade and did not gain any benefit from another's trading.

 

Material information is not limited to historical facts but may also include projections and forecasts. With respect to a future event, such as a merger, acquisition or introduction of a new product, the point at which negotiations or product development are determined to be material is determined by balancing the probability that the event will occur against the magnitude of the effect the event would have on a company's operations or stock price should it occur. Thus, information concerning an event that would have a large effect on stock price, such as a merger, may be material even if the possibility that the event will occur is relatively small. When in doubt about whether particular non-public information is material, presume it is material. If you are unsure whether information is material, you should consult the Compliance Officer before making any decision to disclose such information (other than to persons who need to know it) or to trade in or recommend securities to which that information relates.</p>


<p><i class="fas fa-circle"></i>(b) Non-public Information. Insider trading prohibitions come into play only when you possess information that is material and "non-public." The fact that information has been disclosed to a few members of the public does not make it public for insider trading purposes. To be "public" the information must have been disseminated in a manner designed to reach investors generally, and the investors must be given the opportunity to absorb the information. Even after public disclosure of information about the Company, you must wait until the close of business on the second trading day after the information was publicly disclosed before you can treat the information as public.

 

Non-public information may include:

 

(i) information available to a select group of analysts or brokers or institutional investors;

 

(ii) undisclosed facts that are the subject of rumors, even if the rumors are widely circulated; and

 

(iii) information that has been entrusted to the Company on a confidential basis until a public announcement of the information has been made and enough time has elapsed for the market to respond to a public announcement of the information (normally two or three days).</p>

